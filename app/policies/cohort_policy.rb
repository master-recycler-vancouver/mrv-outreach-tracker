class CohortPolicy < ApplicationPolicy
  def show?
    # any logged in user should be able to see cohorts
    true
  end

  def create?
    user.admin?
  end

  def update?
    user.admin?
  end

  def destroy?
    user.admin?
  end

  class Scope < Scope
    def resolve
      scope.all
    end
  end
end